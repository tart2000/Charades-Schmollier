	if ($i == 1) {
		echo 'Mon premier'; 
	} else if ($i == 2) {
		echo 'Mon deuxième';
	} else if ($i == 3) {
		echo 'Mon troisième';
	} else if ($i == 4) {
		echo 'Mon quatrième';
	} else if ($i == 5) {
		echo 'Mon cinquième';
	} else if ($i == 6) {
		echo 'Mon sixième';
	} else if ($i == 7) {
		echo 'Mon septième';
	} else if ($i == 8) {
		echo 'Mon huitième';
	} else if ($i == 9) {
		echo 'Mon neuvième';
	} else if ($i == 10) {
		echo 'Mon dixième';
	}
}

